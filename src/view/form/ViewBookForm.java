    private void prepareView() {
        try {
            fillComboBooksGenres();
            fillTableBooks();
        } catch (Exception ex) {
            Logger.getLogger(ViewBookForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void fillComboBooksGenres() throws Exception {
        List<BookGenre> genres=Controller.getInstance().getAllGenres();
        cmbGenre.removeAllItems();
        for (BookGenre genre : genres) {
            cmbGenre.addItem(genre);
        }
    }

    public void fillTableBooks() throws Exception {
        List<Book> list=Controller.getInstance().getAllBooks();
        TableModelBooks tmb=new TableModelBooks(list);
        tableBooks.setModel(tmb);
        tmb.fireTableDataChanged();
        
    }
   
