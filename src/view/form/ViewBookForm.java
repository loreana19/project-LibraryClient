    private void btnDeleteBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteBookActionPerformed
       int row=tableBooks.getSelectedRow();
       if(row!=-1){
           TableModelBooks tmb=(TableModelBooks) tableBooks.getModel();
           Book b=tmb.getBook(row);
           try {
               Controller.getInstance().removeBook(b);
               tmb.removeBook(row);
               JOptionPane.showMessageDialog(this, "Sistem je obrisao knjigu");

           } catch (Exception ex) {

               ex.printStackTrace();
              
           }

       }
       else{
           JOptionPane.showMessageDialog(this, "Izaberite knjigu za brisanje");

       }
    }//GEN-LAST:event_btnDeleteBookActionPerformed

    private void btnSearchBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchBookActionPerformed
        try {
            String name=txtBookName.getText().trim();
            BookGenre bg=(BookGenre) cmbGenre.getSelectedItem();
            Book b=new Book(1L, name, null, bg, null, null);
            List<Book> list=Controller.getInstance().getAllBooks();
            List<Book> tableList=new ArrayList<>();
            for (Book book : list) {
                if(book.getBookGenre().getBookGenreID().equals(bg.getBookGenreID()) && book.getName().contains(name))
                    tableList.add(book);
                 }
            
            tableBooks.setModel(new TableModelBooks(tableList));
            if(tableList.size()>0)
                JOptionPane.showMessageDialog(this, "Sistem je našao knjige po zadatoj vrednosti");
            else
                JOptionPane.showMessageDialog(this, "Sistem nije našao knjige po zadatoj vrednosti");

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchBookActionPerformed

