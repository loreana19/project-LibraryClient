    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        int row=TableMember.getSelectedRow();
        if(row!=-1){
        TableModelMembers tmm=(TableModelMembers) TableMember.getModel();
        Member selectedMember=tmm.getMember(row);
        MemberForm mf=new MemberForm(this);
        mf.loadData(selectedMember);

        mf.setVisible(true);

    }else{
            JOptionPane.showMessageDialog(this, "Niste izabrali člana iz tabele");
            return;
        }
    }//GEN-LAST:event_btnDetailsActionPerformed

