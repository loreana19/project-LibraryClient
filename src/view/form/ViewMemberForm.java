    private void btnDeleteMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteMemberActionPerformed
     
       try {  
            int row=TableMember.getSelectedRow();
            
        if(row!=-1){
            TableModelMembers tmm=(TableModelMembers) TableMember.getModel();
            Member m=tmm.getMember(row);

            Controller.getInstance().removeMember(m);
            JOptionPane.showMessageDialog(this, "Član je uspešno obrisan");
            tmm.deleteMember(m);     
        }else{
            JOptionPane.showMessageDialog(this, "Niste izabrali člana za brisanje ");
            return;
        }
            } catch (Exception ex) {
                ex.printStackTrace();
               
            }
      
    }//GEN-LAST:event_btnDeleteMemberActionPerformed

    private void btnSearchMembersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchMembersActionPerformed
        try {
            String firstName=txtFirstName.getText().trim();
            String lastName=txtLastName.getText().trim();
            Member m=new Member(1L, firstName, lastName, "", null);
            List<Member> list=Controller.getInstance().getAllMembers();
            List<Member> tableList=new ArrayList<>();
            for (Member member : list) {
                if(member.getFirstName().contains(firstName) && member.getLastName().contains(lastName)){
                    tableList.add(member);
                }
            }
            
            TableMember.setModel(new TableModelMembers(tableList));
            
            if(tableList.size()>0)
            JOptionPane.showMessageDialog(this, "Sistem je našao članove po zadatoj vrednosti");
            else
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nađe članove po zadatoj vrednosti", "ERROR", JOptionPane.ERROR_MESSAGE);

        } catch (Exception ex) {
            Logger.getLogger(ViewMemberForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }//GEN-LAST:event_btnSearchMembersActionPerformed

