    public void fillTableMembers() throws Exception {
        List<Member> list=Controller.getInstance().getAllMembers();
        TableModelMembers tmm=new TableModelMembers(list);
        TableMember.setModel(tmm);
        tmm.fireTableDataChanged();
    }
