    private void prepareView() {
        try {
            fillTable();
            
        } catch (Exception ex) {
            Logger.getLogger(ViewLoanNoteForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    public void fillTable() {
        try {
            List<LoanNote> ln=Controller.getInstance().getAllLN();
           
            TableModelLN tmln=new TableModelLN(ln);
            tableLN.setModel(tmln);
            tmln.fireTableDataChanged();
        } catch (Exception ex) {
            Logger.getLogger(ViewLoanNoteForm.class.getName()).log(Level.SEVERE, null, ex);
        }
 }
