    private void btnSearchLNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchLNActionPerformed
        try {
            List<LoanNote> listLN=Controller.getInstance().getAllLN();
            List<LoanNote> ln=new ArrayList<>();
           
            String firstName=txtFirstName.getText().trim();
            String lastName=txtLastName.getText().trim();
            String dat=txtCreateDate.getText().trim();
            SimpleDateFormat sdf=new SimpleDateFormat("dd.MM.yyyy");
            Date date=new Date();
           
            if(!txtCreateDate.getText().isEmpty() || !txtFirstName.getText().isEmpty() || !txtLastName.getText().isEmpty()){
             date=sdf.parse(dat);
            
                for (LoanNote loanNote : listLN) {
                    if(loanNote.getDate().equals(date) && loanNote.getMember().getFirstName().contains(firstName) && loanNote.getMember().getLastName().contains(lastName)){
                        ln.add(loanNote);
                    }
                }
                       
            }
             tableLN.setModel(new TableModelLN(ln));

          
            if(ln.size()>0)
             JOptionPane.showMessageDialog(this, "Sistem je našao pozajmice po zadatoj vrednosti");
            else 
             JOptionPane.showMessageDialog(this, "Sistem ne može da nadje pozajmice po zadatoj vrednosti","ERROR",JOptionPane.ERROR_MESSAGE);

   

          
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSearchLNActionPerformed

    private void btnViewLNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewLNActionPerformed
        int row=tableLN.getSelectedRow();
        if(row!=-1){
            TableModelLN tmln=(TableModelLN) tableLN.getModel();
            LoanNote ln=tmln.getLN(row);
         
            LoanNoteForm lnF=new LoanNoteForm(this);
            lnF.loadData(ln);
            lnF.setVisible(true);
            
            
        }else{
            JOptionPane.showMessageDialog(this, "Niste izabrali pozajmicu iz tabele");
            return;
        }
    }//GEN-LAST:event_btnViewLNActionPerformed

